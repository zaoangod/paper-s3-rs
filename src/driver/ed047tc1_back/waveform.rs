//! # 波形数据模块
//!
//! 本模块包含电子墨水屏的波形数据定义。
//!
//! ## 什么是波形？
//!
//! 电子墨水屏通过施加电压来移动墨水微粒，从而改变像素的灰度。
//! 波形（Waveform）定义了在不同条件下如何施加电压序列，
//! 以实现从一个灰度级到另一个灰度级的转换。
//!
//! ## 波形模式
//!
//! ED047TC1 支持以下波形模式：
//!
//! | 模式    | 类型标识      | 名称            | 说明                   | 相位数 | 适用场景             |
//! |---------|---------------|-----------------|------------------------|--------|----------------------|
//! | Mode 1  | INIT/DU       | 初始化/直接更新 | 快速黑白切换，用于清屏 | 5      | 文字显示、快速刷新   |
//! | Mode 2  | GC16          | 全灰度闪烁      | 灰度到灰度，带闪烁更新 | 30     | 图片显示、高质量刷新 |
//! | Mode 5  | GL16          | 全灰度无闪烁    | 灰度到灰度，无闪烁更新 | 30     | 渐变显示、平滑过渡   |
//! | Mode 16 | WHITE_TO_GL16 | 白底到灰度      | 从白色背景过渡到灰度   | 15     | 白色背景优化         |
//! | Mode 17 | BLACK_TO_GL16 | 黑底到灰度      | 从黑色背景过渡到灰度   | 15     | 黑色背景优化         |
//!
//! ## 查找表（LUT）
//!
//! 波形数据中的 LUT 定义了每个相位（phase）中，
//! 不同源灰度级的像素应该如何被驱动。
//! LUT 的格式是 `[相位][源灰度级][4字节打包数据]`。

// ============================================================================
// 波形模式类型枚举
// ============================================================================

/// 波形模式类型
///
/// 定义了 ED047TC1 支持的所有波形模式，使用枚举替代魔数提升类型安全。
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[repr(u8)]
pub enum WaveformModeType {
    /// DU - 直接更新（黑白快速刷新）
    /// 适用于文字显示、快速刷新场景
    Du = 1,
    /// GC16 - 全灰度带闪烁
    /// 适用于图片显示、高质量刷新
    Gc16 = 2,
    /// GL16 - 全灰度无闪烁
    /// 适用于渐变显示、平滑过渡
    Gl16 = 5,
    /// WHITE_TO_GL16 - 白色背景到灰度
    /// epdiy 专有波形，适用于已知屏幕为白色的情况
    WhiteToGl16 = 16,
    /// BLACK_TO_GL16 - 黑色背景到灰度
    /// epdiy 专有波形，适用于已知屏幕为黑色的情况
    BlackToGl16 = 17,
}

impl WaveformModeType {
    /// 从 u8 值转换为枚举
    pub const fn from_u8(value: u8) -> Option<Self> {
        match value {
            1 => Some(Self::Du),
            2 => Some(Self::Gc16),
            5 => Some(Self::Gl16),
            16 => Some(Self::WhiteToGl16),
            17 => Some(Self::BlackToGl16),
            _ => None,
        }
    }

    /// 获取该模式的相位数量
    pub const fn phase_count(&self) -> u8 {
        match self {
            Self::Du => 5,
            Self::Gc16 | Self::Gl16 => 30,
            Self::WhiteToGl16 | Self::BlackToGl16 => 15,
        }
    }

    /// 获取模式名称
    pub const fn name(&self) -> &'static str {
        match self {
            Self::Du => "DU (Direct Update)",
            Self::Gc16 => "GC16 (Full Grayscale with Flash)",
            Self::Gl16 => "GL16 (Full Grayscale No Flash)",
            Self::WhiteToGl16 => "WHITE_TO_GL16",
            Self::BlackToGl16 => "BLACK_TO_GL16",
        }
    }
}

// ============================================================================
// 波形相位结构
// ============================================================================

/// 波形相位数据
///
/// 一个完整的波形由多个相位组成，每个相位有特定的持续时间和 LUT 数据。
///
/// # 字段说明
/// - `phase` - 相位数量，决定了刷新需要多少步
/// - `time` - 每个相位的持续时间（单位：1/10 微秒）
/// - `lut` - 查找表，定义每个相位中每个灰度级的驱动方式
#[derive(Debug, Clone)]
pub struct WaveformPhase {
    /// 相位数量
    /// 例如：Mode 1 (DU) 有 5 个相位，Mode 2 (GC16) 有 30 个相位
    pub phase: u8,

    /// 每个相位的持续时间数组（单位：1/10 微秒）
    /// 数组长度等于 phase
    pub time: &'static [i32],

    /// 查找表数据
    /// 格式：[相位索引][源灰度级 0-15][4 字节打包数据]
    /// 打包数据编码了对应目标灰度级的驱动信号
    pub lut: &'static [[[u8; 4]; 16]],
}

// ============================================================================
// 波形模式结构
// ============================================================================

/// 波形模式配置
///
/// 每种绘图模式（如 DU、GC16、GL16）对应一个波形模式，
/// 可以包含针对不同温度范围的多套相位数据。
#[derive(Debug, Clone)]
pub struct WaveformMode {
    /// 模式类型标识符
    /// 对应 EpdDrawMode 中的值：
    /// - 1 = INIT/DU（直接更新）
    /// - 2 = GC16（全灰度带闪烁）
    /// - 5 = GL16（全灰度无闪烁）
    /// - 16 = WHITE_TO_GL16
    /// - 17 = BLACK_TO_GL16
    pub mode_type: u8,

    /// 温度范围数量
    /// 不同温度下墨水微粒的响应速度不同，
    /// 需要使用不同的波形参数
    pub temperature_range: u8,

    /// 每个温度范围对应的波形相位数据
    /// 数组长度等于 temp_ranges
    pub range_data: &'static [&'static WaveformPhase],
}

// ============================================================================
// 温度区间结构
// ============================================================================

/// 波形温度区间
///
/// 定义波形适用的温度范围（摄氏度）。
/// 电子墨水屏的刷新特性受温度影响较大，低温时墨水微粒移动较慢，需要更长的驱动时间。
#[derive(Debug, Clone, Copy)]
pub struct WaveformTemperatureInterval {
    /// 最低温度（摄氏度）
    pub min: i8,
    /// 最高温度（摄氏度）
    pub max: i8,
}

// ============================================================================
// 完整波形配置
// ============================================================================

/// 显示屏完整波形配置
///
/// 包含一块显示屏所有可用的波形模式和温度配置。
///
/// # 示例
/// ```
/// use ed047tc1_epd::{EPDIY_ED047TC1, EpdDrawMode};
///
/// // 获取 GC16 模式的波形
/// let gc16_mode = EPDIY_ED047TC1.get_mode(2); // 2 = GC16
/// ```
#[derive(Debug, Clone)]
pub struct Waveform {
    /// 可用模式数量
    pub number_mode: u8,
    /// 温度范围数量
    pub number_temperature_range: u8,
    /// 所有可用的波形模式
    pub mode_data: &'static [&'static WaveformMode],
    /// 温度区间配置
    pub temperature_interval: &'static [WaveformTemperatureInterval],
}

impl Waveform {
    /// 根据模式类型获取波形模式（使用 u8）
    ///
    /// # 参数
    /// * `mode_type` - 模式类型（1=DU, 2=GC16, 5=GL16, 16=WHITE_TO_GL16, 17=BLACK_TO_GL16）
    ///
    /// # 返回值
    /// 找到返回 Some(&WaveformMode)，否则返回 None
    pub fn get_mode(&self, mode_type: u8) -> Option<&'static WaveformMode> {
        self.mode_data.iter().find(|m| m.mode_type == mode_type).copied()
    }

    /// 根据模式类型枚举获取波形模式（推荐使用）
    ///
    /// # 参数
    /// * `mode_type` - 波形模式类型枚举
    ///
    /// # 返回值
    /// 找到返回 Some(&WaveformMode)，否则返回 None
    ///
    /// # 示例
    /// ```
    /// use crate::driver::ed047tc1::waveform::{ED047TC1, WaveformModeType};
    /// let gc16 = ED047TC1.get_mode_by_type(WaveformModeType::Gc16);
    /// ```
    pub fn get_mode_by_type(&self, mode_type: WaveformModeType) -> Option<&'static WaveformMode> {
        self.get_mode(mode_type as u8)
    }

    /// 根据温度获取波形相位数据
    ///
    /// # 参数
    /// * `mode` - 波形模式
    /// * `temperature` - 当前环境温度（摄氏度）
    ///
    /// # 返回值
    /// 适用于当前温度的波形相位数据
    ///
    /// # 说明
    /// 如果没有匹配的温度范围，会回退到第一个范围
    pub fn get_phases_for_temperature(&self, mode: &WaveformMode, temperature: i8) -> Option<&'static WaveformPhase> {
        // 遍历温度区间，找到匹配的范围
        for (i, interval) in self.temperature_interval.iter().enumerate() {
            if temperature >= interval.min && temperature <= interval.max {
                if i < mode.range_data.len() {
                    return Some(mode.range_data[i]);
                }
            }
        }
        // 如果没有匹配，回退到第一个范围
        mode.range_data.first().copied()
    }

    /// 获取所有可用的模式类型
    pub fn available_modes(&self) -> impl Iterator<Item = WaveformModeType> + '_ {
        self.mode_data.iter().filter_map(|m| WaveformModeType::from_u8(m.mode_type))
    }

    /// 检查是否支持指定的模式类型
    pub fn supports_mode(&self, mode_type: WaveformModeType) -> bool {
        self.get_mode_by_type(mode_type).is_some()
    }
}

